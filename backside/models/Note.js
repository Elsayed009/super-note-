const mongoose = require('mongoose');

const noteSchema = new mongoose.Schema({
title: {
    type: String,
    required: true
},
content: {
    type: String,
    required: true
},
tags: [{type: String}],

isDeleted: {
    type: Boolean, 
    default: false
},
deleteAt: {
    type: Date,
    default: null
},
expiresAt: {
    type: Date,
    default: null
},
history: [{
    content: String,
    modifiedAt: {type: Date, default: Date.now}
}], 
shareId: {type: String, unique: true, sparse: true},
user: {
    type: mongoose.Schema.Types.ObjectId,
    ref: "User",
    required: true}
}, {timestamps: true});
noteSchema.index({isDeleted: 1, expiresAt: 1});

const Note= mongoose.model('Note', noteSchema);
module.exports = Note;














// حوار الـ Indexing (الفهرسة) ده يا هندسة هو الفرق بين المبرمج اللي بيكتب كود بيشتغل، والمبرمج المحترف اللي بيكتب كود بيستحمل ملايين المستخدمين من غير ما السيرفر يقع أو يستهلك رامات على الفاضي.

// عشان تفهم السطر ده بيعمل إيه بالظبط، تعال نضرب مثال من الواقع (باللوجيك بتاعنا):

// تخيل الداتا بيز كأنها "كتاب فيه مليون صفحة"
// 1. السيناريو الأول (من غير السطر ده - Full Scan):
// تخيل إن الكرون جوب بتاعنا بيسأل الداتا بيز: "يا داتا بيز، هاتيلي كل الملاحظات اللي مش ممسوحة وتاريخها خلص".
// عشان الداتا بيز تجاوب، هتضطر تفتح الـ مليون ملاحظة وتقرأهم واحدة واحدة من أول صفحة لآخر صفحة عشان تدور على المطلوب. العملية دي اسمها في الداتا بيز (Collection Scan أو Full Table Scan)، ودي عملية بطيئة جداً وممكن تهلك السيرفر لو الداتا كبرت.

// 2. السيناريو التاني (لما ضفنا سطر الـ Index):

// السطر ده بيخلي الداتا بيز تعمل "فهرس" (زي فهرس الكتاب اللي في آخر الصفحات) مخصص للحاجتين دول بس (isDeleted و expiresAt).
// لما الكرون جوب يسأل الداتا بيز نفس السؤال، الداتا بيز مش هتقرأ المليون ملاحظة؛ دي هتروح تفتح "الفهرس" وتجيب أرقام الصفحات المطلوبة وتروح لها مباشرة في أجزاء من الثانية! العملية دي اسمها (Index Scan).

// تعال نفصص الكود نفسه: noteSchema.index({ isDeleted: 1, expiresAt: 1 });
// noteSchema.index(): ده الأمر اللي بنقول بيه لـ Mongoose يعمل الفهرس.

// isDeleted و expiresAt: دول الحقلين اللي إحنا بنبحث بيهم دايماً في الكرون جوب (في دالة updateMany). لما بنحط حقلين مع بعض كدة، ده بيتسمى Compound Index (فهرس مركب).

// رقم 1: ده معناه الترتيب التصاعدي (Ascending). يعني رتب الفهرس ده من الصغير للكبير (وممكن نستخدم -1 لو عايزينه تنازلي). في حالتنا دي 1 هيشتغل بشكل مثالي عشان إحنا بنبحث بالتواريخ والحالة.

// ليه مبنعملش Index لكل حاجة في الداتا بيز؟ (معلومة ليك كـ Senior)
// ممكن تسأل نفسك: "طالما هو بيسرع البحث كدة، ليه معملش Index لكل الحقول؟"
// لأن الـ Indexing ليه "ضريبة":

// بياخد مساحة: الفهرس ده بيتخزن في الـ RAM عشان يكون سريع، فلو عملت فهارس كتير هتخلص رامات السيرفر.

// بيبطأ الكتابة: كل ما تيجي تحفظ ملاحظة جديدة أو تعدلها، الداتا بيز مش بس بتحفظها، دي كمان بتضطر تروح تعدل الفهرس عشان تضيفها فيه، فعمليات الـ Create والـ Update بتبقى أبطأ سِنة.

// الخلاصة:
// إحنا عملنا الـ Index ده هنا بالذات لأن الكرون جوب بيشتغل كل ساعة وبيعمل (بحث) في الداتا بيز كلها. فإحنا كدة ضحينا بجزء بسيط جداً من مساحة التخزين مقابل إننا وفرنا مجهود جبار على المعالج (CPU) بتاع السيرفر.